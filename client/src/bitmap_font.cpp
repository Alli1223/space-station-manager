// Textured font shader sources
static const char* fontVertSrc = R"(
#version 330 core
layout(location = 0) in vec2 aPos;
layout(location = 1) in vec2 aTexCoord;
uniform mat4 uProjection;
out vec2 vTexCoord;
void main() {
    gl_Position = uProjection * vec4(aPos, 0.0, 1.0);
    vTexCoord = aTexCoord;
}
)";

static const char* fontFragSrc = R"(
#version 330 core
in vec2 vTexCoord;
out vec4 FragColor;
uniform sampler2D uTexture;
uniform vec4 uColor;
void main() {
    float alpha = texture(uTexture, vTexCoord).r;
    if (alpha < 0.5) discard;
    FragColor = uColor;
}
)";

static GLuint compileShader(GLenum type, const char* src) {
    GLuint s = glCreateShader(type);
    glShaderSource(s, 1, &src, nullptr);
    glCompileShader(s);
    int ok;
    glGetShaderiv(s, GL_COMPILE_STATUS, &ok);
    if (!ok) {
        char log[512];
        glGetShaderInfoLog(s, 512, nullptr, log);
        std::cerr << "Font shader compile error: " << log << std::endl;
    }
    return s;
}

bool BitmapFont::init() {
    // Create shader program
    GLuint vs = compileShader(GL_VERTEX_SHADER, fontVertSrc);
    GLuint fs = compileShader(GL_FRAGMENT_SHADER, fontFragSrc);

    shaderProgram = glCreateProgram();
    glAttachShader(shaderProgram, vs);
    glAttachShader(shaderProgram, fs);
    glLinkProgram(shaderProgram);
    int ok;
    glGetProgramiv(shaderProgram, GL_LINK_STATUS, &ok);
    if (!ok) {
        char log[512];
        glGetProgramInfoLog(shaderProgram, 512, nullptr, log);
        std::cerr << "Font shader link error: " << log << std::endl;
        return false;
    }
    glDeleteShader(vs);
    glDeleteShader(fs);

    uProjection = glGetUniformLocation(shaderProgram, "uProjection");
    uTexture = glGetUniformLocation(shaderProgram, "uTexture");
    uColor = glGetUniformLocation(shaderProgram, "uColor");

    // Create VAO/VBO for quads (each char = 6 vertices, 4 floats each: x, y, u, v)
    glGenVertexArrays(1, &vao);
    glGenBuffers(1, &vbo);
    glBindVertexArray(vao);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, 0, nullptr, GL_DYNAMIC_DRAW);
    glVertexAttribPointer(0, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 4 * sizeof(float), (void*)(2 * sizeof(float)));
    glEnableVertexAttribArray(1);

    // Generate font texture atlas
    generateFontTexture();

    return true;
}

void BitmapFont::generateFontTexture() {
    int atlasW = ATLAS_COLS * CHAR_W;
    int atlasH = ATLAS_ROWS * CHAR_H;
    std::vector<unsigned char> pixels(atlasW * atlasH, 0);

    for (int ch = 0; ch < FONT_CHARS; ch++) {
        int col = ch % ATLAS_COLS;
        int row = ch / ATLAS_COLS;
        int baseX = col * CHAR_W;
        int baseY = row * CHAR_H;

        for (int y = 0; y < CHAR_H; y++) {
            unsigned char rowBits = font8x8[ch][y];
            for (int x = 0; x < CHAR_W; x++) {
                if (rowBits & (0x80 >> x)) {
                    pixels[(baseY + y) * atlasW + (baseX + x)] = 255;
                }
            }
        }
    }

    glGenTextures(1, &textureId);
    glBindTexture(GL_TEXTURE_2D, textureId);
    glTexImage2D(GL_TEXTURE_2D, 0, GL_RED, atlasW, atlasH, 0, GL_RED, GL_UNSIGNED_BYTE, pixels.data());
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
}

void BitmapFont::shutdown() {
    if (textureId) { glDeleteTextures(1, &textureId); textureId = 0; }
    if (shaderProgram) { glDeleteProgram(shaderProgram); shaderProgram = 0; }
    if (vao) { glDeleteVertexArrays(1, &vao); vao = 0; }
    if (vbo) { glDeleteBuffers(1, &vbo); vbo = 0; }
}

void BitmapFont::beginText(int windowWidth, int windowHeight) {
    glUseProgram(shaderProgram);
    // Screen-space projection: origin top-left, Y-down
    glm::mat4 proj = glm::ortho(0.0f, (float)windowWidth, (float)windowHeight, 0.0f, -1.0f, 1.0f);
    glUniformMatrix4fv(uProjection, 1, GL_FALSE, glm::value_ptr(proj));
    glUniform1i(uTexture, 0);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_2D, textureId);
    glBindVertexArray(vao);
}

void BitmapFont::endText() {
    // Restore state — caller should rebind their own shader
}

void BitmapFont::drawText(float x, float y, const std::string& text,
                           float r, float g, float b, float a, float scale) {
    if (text.empty()) return;

    glUniform4f(uColor, r, g, b, a);

    float atlasW = (float)(ATLAS_COLS * CHAR_W);
    float atlasH = (float)(ATLAS_ROWS * CHAR_H);
    float charW = CHAR_W * scale;
    float charH = CHAR_H * scale;

    // Build vertex data for all characters
    std::vector<float> vertices;
    vertices.reserve(text.size() * 6 * 4); // 6 verts * 4 floats each

    float curX = x;
    for (char c : text) {
        if (c < 32 || c > 126) c = '?';
        int idx = c - 32;
        int col = idx % ATLAS_COLS;
        int row = idx / ATLAS_COLS;

        float u0 = (col * CHAR_W) / atlasW;
        float v0 = (row * CHAR_H) / atlasH;
        float u1 = ((col + 1) * CHAR_W) / atlasW;
        float v1 = ((row + 1) * CHAR_H) / atlasH;

        float x0 = curX;
        float y0 = y;
        float x1 = curX + charW;
        float y1 = y + charH;

        // Two triangles per character
        // Triangle 1
        vertices.insert(vertices.end(), {x0, y0, u0, v0});
        vertices.insert(vertices.end(), {x1, y0, u1, v0});
        vertices.insert(vertices.end(), {x0, y1, u0, v1});
        // Triangle 2
        vertices.insert(vertices.end(), {x1, y0, u1, v0});
        vertices.insert(vertices.end(), {x1, y1, u1, v1});
        vertices.insert(vertices.end(), {x0, y1, u0, v1});

        curX += charW;
    }

    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, vertices.size() * sizeof(float), vertices.data(), GL_DYNAMIC_DRAW);
    glDrawArrays(GL_TRIANGLES, 0, static_cast<GLsizei>(vertices.size() / 4));
}

float BitmapFont::measureText(const std::string& text, float scale) const {
    return text.size() * CHAR_W * scale;
}

